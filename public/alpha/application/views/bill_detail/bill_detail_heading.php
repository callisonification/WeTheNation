	<h3 class="bl_title">H.R.5749 Arms Sale Responsibility Act of 2012</h3>
    <p>8/1/2012--Passed House without amendment. (This measure has not been amended since it was introduced. The summary of that version is repeated here.) Stopping Tax Offenders and Prosecuting Identity Theft Act of 2012 or the STOP Identity Theft Act of 2012 - Calls for the Attorney General to: (1) make use of all existing resources of the Department of Justice (DOJ), including task forces, to bring more perpetrators of tax return identity theft to justice; and (2) take into account the need to concentrate efforts in areas of the country where the crime is most frequently reported, to coordinate with state and local authorities to prosecute and prevent such crime, and to protect vulnerable groups from becoming victims or otherwise being used in the offense. Amends the federal criminal code to: (1) define &amp;quot;means of identification&amp;quot; to mean any name or number used to identify a specific person (currently, any specific individual); and (2) make tax fraud a predicate offense for aggravated identity theft. Directs the Attorney General to include in the first annual DOJ performance report made more than nine months after the date of this Act's enactment information as to progress in implementing this Act regarding: (1) information readily available to DOJ about trends in the incidence of tax return identity theft, (2) the effectiveness of statutory tools in aiding DOJ in prosecuting it, (3) recommendations on additional statutory tools that would aid in removing barriers to effective prosecution, and (4) the status of implementing DOJ's March 2010 audit report on DOJ efforts to combat identity theft.</p>
